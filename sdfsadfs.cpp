
#include<stdio.h>

int main()
{   

    int t;
    scanf("%d",&t); // taking inputs 
    printf("%d\n",t); // for output on the console screen

    long int j;

    scanf("%ld",&j);
    printf("%ld\n",j);

    long long int k;
     
    scanf("%lld",&k);
    printf("%lld\n",k);

    //string literal  %s
    char str[100];
    scanf("%s",str);
    printf("%s\n",str);

    return 0;
}

 

